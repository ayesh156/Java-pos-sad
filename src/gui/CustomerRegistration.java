    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String mobile = jTextField1.getText();
        String fname = jTextField2.getText();
        String lname = jTextField4.getText();
        String email = jTextField5.getText();
        
        if(mobile.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please enter customer mobile", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")){
            
            JOptionPane.showMessageDialog(this, "Invalid mobile", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else if (fname.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please enter customer first name", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else if (lname.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please enter customer last name", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else if (email.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please enter customer email", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@" 
        + "[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")){
            
            JOptionPane.showMessageDialog(this, "Invalid email", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else {
            
            try {
                
                ResultSet resultset = MySQL.execute("SELECT * FROM `customer` WHERE `mobile`='"+mobile+"' OR `email` = '"+email+"' ");
                
                if(resultset.next()){
                    JOptionPane.showMessageDialog(this, "Customer already registered", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    
                    MySQL.execute("INSERT INTO `customer` (`mobile`,`first_name`,`last_name`,`email`,`points`) "
                            + "VALUES ('"+mobile+"','"+fname+"','"+lname+"','"+email+"','0')");
                    
                    reset();
                    loadCustomers("first_name", "ASC","07");
                    
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

