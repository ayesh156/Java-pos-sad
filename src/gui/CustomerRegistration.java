    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        jButton2.setEnabled(true);
        jTextField1.setEditable(false);
        
        int row = jTable1.getSelectedRow();
        
        String mobile = String.valueOf(jTable1.getValueAt(row, 0));
        String fname = String.valueOf(jTable1.getValueAt(row, 1));
        String lname = String.valueOf(jTable1.getValueAt(row, 2));
        String email = String.valueOf(jTable1.getValueAt(row, 3));
        
        jTextField1.setText(mobile);
        jTextField2.setText(fname);
        jTextField4.setText(lname);
        jTextField5.setText(email);
        
        try {
            
            ResultSet resultset = MySQL.execute("SELECT COUNT(`id`) FROM `invoice` WHERE `customer_mobile`='"+mobile+"'");
            
            if(resultset.next()){
                String count = resultset.getString(1);
                jLabel7.setText(count);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        if(evt.getClickCount()==2){
            if(invoice!=null){
                invoice.getjTextField2().setText(String.valueOf(jTable1.getValueAt(row, 0)));
                invoice.getjLabel22().setText(String.valueOf(jTable1.getValueAt(row, 1)) + " " + String.valueOf(jTable1.getValueAt(row, 2)));
                invoice.getjFormattedTextField5().setText(String.valueOf(jTable1.getValueAt(row, 4)));
                this.dispose();
            }
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String mobile = jTextField1.getText();
        String fname = jTextField2.getText();
        String lname = jTextField4.getText();
        String email = jTextField5.getText();
        
        if (fname.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please enter customer first name", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else if (lname.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please enter customer last name", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else if (email.isEmpty()){
            
            JOptionPane.showMessageDialog(this, "Please enter customer email", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@" 
        + "[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")){
            
            JOptionPane.showMessageDialog(this, "Invalid email", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else {
            
            try {
                
                ResultSet resultset = MySQL.execute("SELECT * FROM `customer` WHERE `email` = '"+email+"' ");
                
                boolean canUpdate = false;
                
                if(resultset.next()){
                    
                    if(resultset.getString("mobile").equals(mobile)){
                        canUpdate = true;
                    } else {
                        JOptionPane.showMessageDialog(this, "Email already registered", "Warning", JOptionPane.WARNING_MESSAGE);
                    }
                    
                } else {
                    
                    canUpdate = true;
                    
                }
                
                if(canUpdate) {
                    MySQL.execute("UPDATE `customer` SET `first_name`='"+fname+"',`last_name`='"+lname+"',`email`='"+email+"' "
                            + "WHERE `mobile` = '"+mobile+"'");
                    
                    reset();
                    loadCustomers("first_name", "ASC","07");
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

